// Shared Booking Data Model

export type BookingType =
  | 'accommodation'
  | 'flight'
  | 'rail'
  | 'transfer'
  | 'dining'
  | 'experience'
  | 'wellness';

export type BookingStatus =
  | 'confirmed'
  | 'pending'
  | 'updated'
  | 'awaiting'
  | 'cancelled';

export type BookingAction =
  | 'view-document'
  | 'message-sofia'
  | 'request-change'
  | 'add-to-calendar'
  | 'view-location';

export interface DetailRow {
  label: string;
  value: string;
  mono?: boolean;
  span?: boolean;
}

export interface Booking {
  id: string;
  journeyId: string;
  type: BookingType;
  status: BookingStatus;
  title: string;
  subtitle?: string;
  date?: string;
  startTime?: string;
  endTime?: string;
  duration?: string;
  location?: string;
  address?: string;
  confirmationRef?: string;
  description?: string;
  notes?: string;
  conciergeNote?: string;
  cancellationTerms?: string;
  details: DetailRow[];
  inclusions?: string[];
  whatToBring?: string[];
  actions: BookingAction[];
  calendarDate?: string;
  calendarStart?: string;
  calendarEnd?: string;
  timezone?: string;
  mapQuery?: string;
  relatedDocId?: number;
}

export function getBooking(id: string): Booking | undefined {
  return BOOKINGS.find(b => b.id === id);
}

export function getJourneyBookings(journeyId: string): Booking[] {
  return BOOKINGS.filter(b => b.journeyId === journeyId);
}

